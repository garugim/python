# 숫자 두개를 입력을 받아서
# +, -, *, / 를 출력하는 프로그램을 만들어 보자

# 두 개의 숫자를 입력받고 사칙연산을 수행하는 프로그램

# 숫자 입력 받기
a = float(input("첫 번째 숫자를 입력하세요: "))
b = float(input("두 번째 숫자를 입력하세요: "))

# 연산 수행 및 출력
print(f"{a} + {b} = {a + b}")  # 덧셈 출력
print(f"{a} - {b} = {a - b}")  # 뺄셈 출력
print(f"{a} * {b} = {a * b}")  # 곱셈 출력

# 나눗셈 (0으로 나누는 것 방지)
if b != 0:
    print(f"{a} / {b} = {a / b}")
else:
    print("0으로 나눌 수 없습니다.")
